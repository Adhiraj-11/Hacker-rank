s = input()


for i in s:

    if i.isalnum() is True:
        print(True)
    
        break

else:
    print(False)

for i in s:

    if i.isalpha() is True:
        print(True)

        break

else:
    print(False)


for i in s:

    if i.isdigit() is True:
        print(True)
    
        break

else:
    print(False)


for i in s:

    if i.islower() is True:
        print(True)
    
        break

else:
    print(False)

for i in s:

    if i.isupper() is True:
        print(True)
    
        break

else:
    print(False)


